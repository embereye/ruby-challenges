weather_now = "humid"

case weather_now
when "rainy"
  puts "Get that umbrella!"
when "cloudy"
  puts "Sun will come back tomorrow, maybe!"
when "sunny"
  puts "Sun screen and shades!"
when "windy"
  puts "Hold onto your butts!"
when "hurricane"
  puts "Do not go outside"
when "humid"
  puts "You now live in soup."
else
  puts "Hmm, well... I guess it's some weather I haven't thought of!"
end
